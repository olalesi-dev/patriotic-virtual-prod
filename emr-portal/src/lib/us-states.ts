const US_STATE_NAME_TO_CODE = new Map<string, string>(
    US_STATE_OPTIONS.map((state) => [state.name.toLowerCase(), state.code])
);

const US_STATE_CODE_SET = new Set<string>(US_STATE_OPTIONS.map((state) => state.code));

export function normalizeUsStateCode(value: string | null | undefined): string | null {
    if (!value) return null;
    const trimmed = value.trim();
    if (!trimmed) return null;

    const upperCode = trimmed.toUpperCase();
    if (US_STATE_CODE_SET.has(upperCode)) {
        return upperCode;
    }

    return US_STATE_NAME_TO_CODE.get(trimmed.toLowerCase()) ?? null;
}
